    NOMBRE_PRODUCTO IN PRODUCTO.NOMBRE_PRODUCTO%TYPE,
    PRECIO IN PRODUCTO.PRECIO%TYPE,
    ID_CALIDAD IN PRODUCTO.ID_CALIDAD%TYPE,
    PORCENTAJE_MERMA IN PRODUCTO.PORCENTAJE_MERMA%TYPE
) IS


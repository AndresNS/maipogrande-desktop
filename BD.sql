-- ACTUALIZAR PRODUCTO
CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_PRODUCTO (
    ID IN PRODUCTO.ID_PRODUCTO%TYPE,
    CATEGORIA IN PRODUCTO.ID_CATEGORIA%TYPE,
    NOMBRE IN PRODUCTO.NOMBRE_PRODUCTO%TYPE,
    PRECIO_PRODUCTO IN PRODUCTO.PRECIO%TYPE,
    CALIDAD IN PRODUCTO.ID_CALIDAD%TYPE,
    MERMA IN PRODUCTO.PORCENTAJE_MERMA%TYPE

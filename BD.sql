-- ELIMINAR PRODUCTO
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_PRODUCTO (ID IN NUMBER) IS

BEGIN
    DELETE FROM PRODUCTO 
    WHERE  ID_PRODUCTO = ID;
    
    COMMIT;
END;

-- INGRESAR USUARIO
CREATE OR REPLACE PROCEDURE SP_INGRESAR_USUARIO (
    NOMBRE_USUARIO IN USUARIO.NOMBRE_USUARIO%TYPE,
    PASSWORD IN USUARIO.PASSWORD%TYPE,
    FECHA_CREACION IN USUARIO.FECHA_CREACION%TYPE,
    ID_PERFIL IN USUARIO.ID_PERFIL%TYPE,
    ID_ESTD_CTA IN USUARIO.ID_ESTD_CTA%TYPE
) IS

BEGIN
    INSERT INTO USUARIO ("ID_USUARIO", "NOMBRE_USUARIO", "PASSWORD", "FECHA_CREACION", "ID_PERFIL", "ID_ESTD_CTA") 
    VALUES (USUARIO_SEQ.NEXTVAL, NOMBRE_USUARIO, PASSWORD, FECHA_CREACION, ID_PERFIL, ID_ESTD_CTA);
    
    COMMIT;
END;
/

-- ELIMINAR USUARIO
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_USUARIO (ID IN NUMBER) IS

BEGIN
    DELETE FROM USUARIO 
    WHERE  ID_USUARIO = ID;
    
    COMMIT;
END;
/

-- ACTUALIZAR USUARIO
CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_USUARIO (
    ID IN USUARIO.ID_USUARIO%TYPE,
    NOMBRE IN USUARIO.NOMBRE_USUARIO%TYPE,
    PASS IN USUARIO.PASSWORD%TYPE,
    FECHA IN USUARIO.FECHA_CREACION%TYPE,
    PERFIL IN USUARIO.ID_PERFIL%TYPE,
    ESTD_CTA IN USUARIO.ID_ESTD_CTA%TYPE
) IS

BEGIN
    UPDATE USUARIO 
    SET 
    NOMBRE_USUARIO = NOMBRE,
    PASSWORD = PASS,
    FECHA_CREACION = FECHA,
    ID_PERFIL = PERFIL,
    ID_ESTD_CTA = ESTD_CTA
    WHERE ID_USUARIO = ID;
    
    COMMIT;
END;
/

